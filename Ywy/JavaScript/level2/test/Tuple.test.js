import Tuple from '../Tuple';

test(`"{{2},{2,1},{2,1,3},{2,1,3,4}}" is [2, 1, 3, 4]`, () => {
    expect(new Tuple().solution("{{2},{2,1},{2,1,3},{2,1,3,4}}")).toEqual([2, 1, 3, 4]);
});

test(`"{{1,2,3},{2,1},{1,2,4,3},{2}}" is [2, 1, 3, 4]`, () => {
    expect(new Tuple().solution("{{1,2,3},{2,1},{1,2,4,3},{2}}")).toEqual([2, 1, 3, 4]);
});
